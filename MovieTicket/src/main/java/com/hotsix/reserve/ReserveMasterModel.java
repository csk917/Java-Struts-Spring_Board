package com.hotsix.reserve;

public class ReserveMasterModel {

	String reserve_master_no;
	int member_no, time_detail_no, adult, child, total_amount;
	
	
	public String getReserve_master_no() {
		return reserve_master_no;
	}
	public void setReserve_master_no(String reserve_master_no) {
		this.reserve_master_no = reserve_master_no;
	}
	public int getMember_no() {
		return member_no;
	}
	public void setMember_no(int member_no) {
		this.member_no = member_no;
	}
	public int getTime_detail_no() {
		return time_detail_no;
	}
	public void setTime_detail_no(int time_detail_no) {
		this.time_detail_no = time_detail_no;
	}
	public int getAdult() {
		return adult;
	}
	public void setAdult(int adult) {
		this.adult = adult;
	}
	public int getChild() {
		return child;
	}
	public void setChild(int child) {
		this.child = child;
	}
	public int getTotal_amount() {
		return total_amount;
	}
	public void setTotal_amount(int total_amount) {
		this.total_amount = total_amount;
	}
	
}
